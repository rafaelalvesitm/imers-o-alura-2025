# --- Fun√ß√£o auxiliar para chamar agente
def chamar_agente(agent: Agent, message_text: str) -> str:
    # Cria um servi√ßo de sess√£o em mem√≥ria
    session_service = InMemorySessionService()
    # Cria uma nova sess√£o (voc√™ pode personalizar os IDs conforme necess√°rio)
    # Nota: Criar uma nova sess√£o para CADA chamada pode n√£o manter contexto entre chamadas
    # Para manter contexto, voc√™ reutilizaria a sess√£o ou passaria hist√≥rico.
    # Para este exemplo simples de dispatcher, uma nova sess√£o √© OK.
    session = session_service.create_session(app_name=agent.name, user_id="user1", session_id="session1")
    # Cria um Runner para o agente
    runner = Runner(agent=agent, app_name=agent.name, session_service=session_service)
    # Cria o conte√∫do da mensagem de entrada
    content = types.Content(role="user", parts=[types.Part(text=message_text)])

    final_response = ""
    # Itera assincronamente pelos eventos retornados durante a execu√ß√£o do agente
    for event in runner.run(user_id="user1", session_id="session1", new_message=content):
        if event.is_final_response():
          for part in event.content.parts:
            if part.text is not None:
              final_response += part.text
              final_response += "\n"
    return final_response.strip() # Remove espa√ßos/linhas extras no final

def extrai_texto_pdf_para_json(pdf_paths: list, output_json_path: str) -> dict:
    """
    Extrai texto de m√∫ltiplos arquivos PDF, armazenando o conte√∫do em um dicion√°rio
    onde as chaves s√£o os nomes dos arquivos e os valores s√£o os seus conte√∫dos.
    O dicion√°rio resultante √© salvo em um arquivo JSON.

    Args:
        pdf_paths: Uma lista de caminhos para os arquivos PDF a serem lidos.
        output_json_path: O caminho completo para o arquivo JSON onde o dicion√°rio ser√° salvo.

    Returns:
        Um dicion√°rio onde as chaves s√£o os nomes dos arquivos e os valores s√£o
        os seus conte√∫dos, ou um dicion√°rio parcialmente preenchido/vazio em caso de erro
        na leitura de alguns arquivos.
    """
    document_data = {}

    print(f"Iniciando a extra√ß√£o de texto de arquivos PDF: {pdf_paths}")

    for pdf_path in pdf_paths:
        # Pega apenas o nome do arquivo para usar como chave no dicion√°rio
        filename = os.path.basename(pdf_path)

        try:
            # Abre o documento PDF
            doc = fitz.open(pdf_path)
            full_text = ""

            # Extrai texto de cada p√°gina e concatena
            for page_num in range(doc.page_count):
                page = doc.load_page(page_num)
                # Extrai texto. 'sort=True' tenta ordenar o texto lido por posi√ß√£o.
                text = page.get_text("text", sort=True)
                full_text += text + "\n\n--- Nova P√°gina ---\n\n" # Adiciona separador entre p√°ginas

            doc.close() # Fecha o documento PDF

            # Armazena o texto extra√≠do no dicion√°rio
            document_data[filename] = full_text.strip() # Remove espa√ßos em branco extras do in√≠cio/fim
            print(f"Texto do arquivo '{filename}' extra√≠do com sucesso.")

        except fitz.FileNotFoundError:
            print(f"Erro: O arquivo PDF n√£o foi encontrado em: {pdf_path}")
